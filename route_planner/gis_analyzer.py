def get_nearby_water_stations(coords: List[Tuple], max_dist_m: float = 1200) -> List[Dict]:
    """获取路线附近的水站（v6.0：搜索半径从800m扩大到1200m，适配北部路线）"""
    result = []
    seen = set()
    for ws in WATER_STATIONS:
        for lat, lon in coords[::5]:
            d = haversine(lat, lon, ws["lat"], ws["lon"])
            if d <= max_dist_m and ws["name"] not in seen:
                result.append(ws)
                seen.add(ws["name"])
                break
    return result


